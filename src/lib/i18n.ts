/** Interpolate {key} placeholders */
function interpolate(str: string, params?: Record<string, string | number>): string {
  if (!params || typeof str !== 'string') return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? `{${k}}`));
}

/** Traverse nested key path like "common.save" */
function resolve(obj: any, path: string): any {
  return path.split('.').reduce((acc, k) => acc?.[k], obj);
}

export function t(
  lang: Lang,
  key: string,
  params?: Record<string, string | number>
): string {
  const val = resolve(dict[lang], key) ?? resolve(dict['en'], key) ?? key;
  if (typeof val === 'string') return interpolate(val, params);
  return String(val);
}

export function tArr(lang: Lang, key: string): string[] {
  const val = resolve(dict[lang], key) ?? resolve(dict['en'], key) ?? [];
  return Array.isArray(val) ? val : [];
}

